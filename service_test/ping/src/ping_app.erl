-module(ping_app).

-behaviour(application).

%% Application callbacks
-export([start/2
        ,stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    Routes = [
        {'_', [
            {"/", http_ping, []}
        ]}
    ],
    Dispatch = cowboy_router:compile(Routes),
    {ok, _} = cowboy:start_http(http, 200, [{port, 8888}], [{env, [{dispatch, Dispatch}]}]),
    ping_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

